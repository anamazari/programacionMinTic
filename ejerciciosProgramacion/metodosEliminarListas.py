#Método pop
lista_compras = ["leche","huevos","pan","lechuga"]
print("Esta es tu lista de compra: ", lista_compras)

lista_compras.pop(0)

print("Esta es tu nueva lista de compra: ", lista_compras)

#Método remove
lista_compras = ["leche","huevos","pan","lechuga"]
print("Esta es tu lista de compra: ", lista_compras)

lista_compras.remove("huevos")

print("Esta es tu nueva lista de compra: ", lista_compras)